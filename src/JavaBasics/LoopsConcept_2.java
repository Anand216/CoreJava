package JavaBasics;

public class LoopsConcept_2 {

	public static void main(String[] args) {
		
		for (int i = 1; i < 5; i++) {//initialization, condition, increment or decrement
			System.out.println("Tom "+i);
		}
		System.out.println("=======");
		
		for (int a = 5; a > 1; a--) {
			System.out.println("Ted"+a);
		}
		System.out.println("++++++");
		int j = 1; //initialization
		while (j < 5) { //condition
			System.out.println("Trump"+j);
			j++; //increment or decrement
		}

	}

}
